        _tags = d.pop("tags", UNSET)
        for tags_item_data in _tags or []:
            tags_item = Tag.from_dict(tags_item_data)

            tags.append(tags_item)

        tags = cls(
            tags=tags,
        )

        tags.additional_properties = d
        return tags

